  env JAZZ_SMOKE=1 cargo bench -p jazz-sim --bench s5_durable_stream

run_scenario \
  "jazz-sim/s6_text_traces" \
  "JAZZ_SMOKE=1 cargo bench -p jazz-sim --bench s6_text_traces" \
  env JAZZ_SMOKE=1 cargo bench -p jazz-sim --bench s6_text_traces

run_scenario \
  "jazz-sim/s7_migrations" \
  "cargo bench -p jazz-sim --bench s7_migrations # source always runs smoke(); no env knob" \
  cargo bench -p jazz-sim --bench s7_migrations

run_scenario \
  "jazz-sim/s9_durable_execution" \
  "JAZZ_SMOKE=1 cargo bench -p jazz-sim --bench s9_durable_execution" \
  env JAZZ_SMOKE=1 cargo bench -p jazz-sim --bench s9_durable_execution

if ((${#failures[@]})); then
  printf '\nFailures:\n'
  for failure in "${failures[@]}"; do
    printf '  - %s\n' "$failure"
  done
  printf '\nLedger written to %s\n' "$LEDGER"
  exit 1
fi

printf '\nAll smoke benchmark scenarios passed.\n'
printf 'Ledger written to %s\n' "$LEDGER"
