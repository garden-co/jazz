  "JAZZ_MICRO_ITERS=1 cargo bench -p jazz-sim --bench micro # no JAZZ_SMOKE; fixed size ladders remain" \
  env JAZZ_MICRO_ITERS=1 cargo bench -p jazz-sim --bench micro

