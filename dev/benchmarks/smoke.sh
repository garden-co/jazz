  "jazz-sim/s1_saas" \
  "JAZZ_SMOKE=1 cargo bench -p jazz-sim --bench s1_saas" \
  env JAZZ_SMOKE=1 cargo bench -p jazz-sim --bench s1_saas

run_scenario \
  "jazz-sim/s2_canvas" \
  "JAZZ_SMOKE=1 cargo bench -p jazz-sim --bench s2_canvas" \
  env JAZZ_SMOKE=1 cargo bench -p jazz-sim --bench s2_canvas

run_scenario \
  "jazz-sim/s3_permissions" \
  "JAZZ_SMOKE=1 cargo bench -p jazz-sim --bench s3_permissions" \
  env JAZZ_SMOKE=1 cargo bench -p jazz-sim --bench s3_permissions

run_scenario \
  "jazz-sim/s4_order_processing" \
  "JAZZ_SMOKE=1 cargo bench -p jazz-sim --bench s4_order_processing" \
  env JAZZ_SMOKE=1 cargo bench -p jazz-sim --bench s4_order_processing

run_scenario \
