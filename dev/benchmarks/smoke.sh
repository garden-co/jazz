#!/usr/bin/env bash
set -u -o pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
LEDGER="$ROOT/dev/benchmarks/SMOKE_LEDGER.md"
LOG_DIR="$ROOT/target/benchmark-smoke"

mkdir -p "$LOG_DIR"

cat >"$LEDGER" <<'LEDGER'
# Benchmark Smoke Ledger

Generated by `dev/benchmarks/smoke.sh`.

| Scenario | Invocation | Status | Wall time | Counters / panic |
| --- | --- | --- | --- | --- |
LEDGER

failures=()

escape_md() {
  local value="$1"
  value="${value//$'\n'/<br>}"
  value="${value//|/\\|}"
  printf '%s' "$value"
}

summarize_output() {
  local log="$1"
  if grep -q "panicked at" "$log"; then
    grep -A3 -B1 "panicked at" "$log" | tail -n 8
    return
  fi
  if grep -q "assertion .*failed\\|assertion failed\\|thread '.*' panicked" "$log"; then
    grep -E "assertion .*failed|assertion failed|thread '.*' panicked" "$log" | tail -n 8
    return
  fi
  grep -E '(^\{|^[[]|^[[:alnum:]_ -]+[=:]|_us|_ms|_ns|count|bytes|rows|commits|depth|scenario|phase)' "$log" | tail -n 12
}

run_scenario() {
  local name="$1"
  local invocation="$2"
  shift 2
  local log="$LOG_DIR/${name//[^A-Za-z0-9_.-]/_}.log"
  local start end elapsed status detail

  printf '==> %s\n' "$name"
  start="$(perl -MTime::HiRes=time -e 'print time')"
  (
    cd "$ROOT"
    "$@"
  ) >"$log" 2>&1
  status=$?
  end="$(perl -MTime::HiRes=time -e 'print time')"
  elapsed="$(awk -v start="$start" -v end="$end" 'BEGIN { printf "%.3fs", end - start }')"

  if [[ $status -eq 0 ]]; then
    status_text="pass"
  elif grep -q "panicked at\\|thread '.*' panicked" "$log"; then
    status_text="panic"
  else
    status_text="fail"
  fi

  detail="$(summarize_output "$log")"
  if [[ -z "$detail" ]]; then
    detail="(no counters printed; see $log)"
  fi

  printf '| %s | `%s` | %s | %s | %s |\n' \
    "$(escape_md "$name")" \
    "$(escape_md "$invocation")" \
    "$status_text" \
    "$elapsed" \
    "$(escape_md "$detail")" >>"$LEDGER"

  if [[ $status -ne 0 ]]; then
    failures+=("$name ($status_text)")
  fi
}

note_scenario() {
  local name="$1"
  local invocation="$2"
  local note="$3"
  printf '| %s | `%s` | not run | - | %s |\n' \
    "$(escape_md "$name")" \
    "$(escape_md "$invocation")" \
    "$(escape_md "$note")" >>"$LEDGER"
}

run_scenario \
  "jazz/cold_subscription" \
  "JAZZ_DEPTHS=100 JAZZ_PENDING_SIZES=0,10 cargo bench -p jazz --bench cold_subscription" \
  env JAZZ_DEPTHS=100 JAZZ_PENDING_SIZES=0,10 cargo bench -p jazz --bench cold_subscription

