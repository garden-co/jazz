#!/usr/bin/env bash
set -u -o pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
LEDGER="${SMOKE_LEDGER:-$ROOT/dev/benchmarks/SMOKE_LEDGER.md}"
LOG_DIR="${SMOKE_LOG_DIR:-$ROOT/target/benchmark-smoke}"
RESULT_ROOT="${SMOKE_RESULT_ROOT:-$ROOT/dev/benchmarks/results}"
RUN_LABEL="${SMOKE_LABEL:-smoke}"
RUN_NOTES="${SMOKE_NOTES:-}"
EXCERPT_LINES="${SMOKE_LEDGER_EXCERPT_LINES:-18}"
RUN_ID="$(date -u +%Y%m%dT%H%M%SZ)"
RESULT_DIR="$RESULT_ROOT/$RUN_ID"
MANIFEST="$RESULT_DIR/manifest.tsv"
PREBUILD_S="0.000000"

mkdir -p "$LOG_DIR" "$RESULT_DIR"
: >"$MANIFEST"

failures=()

json_escape() {
  python3 -c 'import json, sys; print(json.dumps(sys.argv[1]))' "$1"
}

json_number_from_seconds() {
  awk -v seconds="$1" 'BEGIN { printf "%.0f", seconds * 1000000 }'
}

git_dirty() {
  if git -C "$ROOT" diff --quiet && git -C "$ROOT" diff --cached --quiet; then
    printf 'false'
  else
    printf 'true'
  fi
}

prebuild_benches() {
  local start end status log
  log="$LOG_DIR/prebuild.log"
  printf '==> prebuild smoke bench binaries\n'
  start="$(perl -MTime::HiRes=time -e 'print time')"
  (
    cd "$ROOT"
    cargo bench -p jazz --no-run -j 2
    cargo bench -p jazz-sim --no-run -j 2
  ) >"$log" 2>&1
  status=$?
  end="$(perl -MTime::HiRes=time -e 'print time')"
  PREBUILD_S="$(awk -v start="$start" -v end="$end" 'BEGIN { printf "%.6f", end - start }')"
  if [[ $status -ne 0 ]]; then
    failures+=("prebuild (fail)")
  fi

  if [[ "${SMOKE_PREBUILD_PROFILING:-0}" == "1" ]]; then
    local profile_start profile_end profile_status
    printf '==> prebuild profiling bench binaries\n'
    profile_start="$(perl -MTime::HiRes=time -e 'print time')"
    (
      cd "$ROOT"
      cargo bench -p jazz-sim --no-run -j 2 --features profiling
    ) >>"$log" 2>&1
    profile_status=$?
    profile_end="$(perl -MTime::HiRes=time -e 'print time')"
    PREBUILD_S="$(
      awk -v base="$PREBUILD_S" -v start="$profile_start" -v end="$profile_end" \
        'BEGIN { printf "%.6f", base + end - start }'
    )"
    if [[ $profile_status -ne 0 ]]; then
      failures+=("profiling prebuild (fail)")
    fi
  fi
}

append_ledger_header_if_needed() {
  if [[ -s "$LEDGER" ]]; then
    return
  fi

  cat >"$LEDGER" <<'LEDGER'
# Benchmark Smoke Ledger

Append-only smoke benchmark runs generated by `dev/benchmarks/smoke.sh`.

Each run keeps a compact status summary plus per-scenario excerpts. Full raw
stdout/stderr logs live under `target/benchmark-smoke/`; machine-readable JSONL
artifacts live under `dev/benchmarks/results/` and are intentionally gitignored.
LEDGER
}

extract_json_lines() {
  local log="$1"
  local jsonl="$2"
  python3 - "$log" "$jsonl" <<'PY'
import json
import sys
from pathlib import Path

log = Path(sys.argv[1])
jsonl = Path(sys.argv[2])
count = 0
with log.open("r", encoding="utf-8", errors="replace") as src, jsonl.open(
    "w", encoding="utf-8"
) as dest:
    for line in src:
        text = line.strip()
        if not text.startswith("{"):
            continue
        try:
            obj = json.loads(text)
        except json.JSONDecodeError:
            continue
        dest.write(json.dumps(obj, sort_keys=True, separators=(",", ":")) + "\n")
        count += 1
print(count)
PY
}

append_harness_json() {
  local jsonl="$1"
  local name="$2"
  local status="$3"
  local elapsed="$4"
  local invocation="$5"
  local log="$6"
  local emitted="$7"
  local wall_us
  wall_us="$(json_number_from_seconds "$elapsed")"
  {
    printf '{'
    printf '"scenario":%s,' "$(json_escape "$name")"
    printf '"phase":"harness",'
    printf '"status":%s,' "$(json_escape "$status")"
    printf '"wall_us":%s,' "$wall_us"
    printf '"wall_s":%.6f,' "$elapsed"
    printf '"emitted_json_lines":%s,' "$emitted"
    printf '"invocation":%s,' "$(json_escape "$invocation")"
    printf '"log":%s' "$(json_escape "${log#$ROOT/}")"
    printf '}\n'
  } >>"$jsonl"
}

record_manifest() {
  local name="$1"
  local invocation="$2"
  local status="$3"
  local elapsed="$4"
  local log="$5"
  local jsonl="$6"
  printf '%s\t%s\t%s\t%s\t%s\t%s\n' \
    "$name" "$invocation" "$status" "$elapsed" "${log#$ROOT/}" "${jsonl#$ROOT/}" >>"$MANIFEST"
}

run_scenario() {
  local name="$1"
  local invocation="$2"
  shift 2
  local safe_name log jsonl start end elapsed status status_text emitted

  safe_name="${name//[^A-Za-z0-9_.-]/_}"
  log="$LOG_DIR/$safe_name.log"
  jsonl="$RESULT_DIR/$safe_name.jsonl"

  printf '==> %s\n' "$name"
  start="$(perl -MTime::HiRes=time -e 'print time')"
  (
    cd "$ROOT"
    "$@"
  ) >"$log" 2>&1
  status=$?
  end="$(perl -MTime::HiRes=time -e 'print time')"
  elapsed="$(awk -v start="$start" -v end="$end" 'BEGIN { printf "%.6f", end - start }')"

  if [[ $status -eq 0 ]]; then
    status_text="pass"
  elif grep -q "panicked at\\|thread '.*' panicked" "$log"; then
    status_text="panic"
  else
    status_text="fail"
  fi

  emitted="$(extract_json_lines "$log" "$jsonl")"
  append_harness_json "$jsonl" "$name" "$status_text" "$elapsed" "$invocation" "$log" "$emitted"
  record_manifest "$name" "$invocation" "$status_text" "$elapsed" "$log" "$jsonl"

  if [[ $status -ne 0 ]]; then
    failures+=("$name ($status_text)")
  fi
}

append_ledger_run() {
  local run_at sha dirty result
  run_at="$(date -u +%Y-%m-%dT%H:%M:%SZ)"
  sha="$(git -C "$ROOT" rev-parse --short HEAD)"
  dirty="$(git_dirty)"
  if ((${#failures[@]})); then
    result="fail"
  else
    result="pass"
  fi

  append_ledger_header_if_needed

  LEDGER="$LEDGER" \
  ROOT="$ROOT" \
  MANIFEST="$MANIFEST" \
  RESULT_DIR="$RESULT_DIR" \
  RUN_AT="$run_at" \
  RUN_LABEL="$RUN_LABEL" \
  RUN_NOTES="$RUN_NOTES" \
  RUN_RESULT="$result" \
  RUN_SHA="$sha" \
  RUN_DIRTY="$dirty" \
  PREBUILD_S="$PREBUILD_S" \
  EXCERPT_LINES="$EXCERPT_LINES" \
  python3 <<'PY' >>"$LEDGER"
import json
import os
import re
from pathlib import Path

ledger = Path(os.environ["LEDGER"])
root = Path(os.environ["ROOT"])
manifest = Path(os.environ["MANIFEST"])
result_dir = Path(os.environ["RESULT_DIR"])
excerpt_lines = int(os.environ["EXCERPT_LINES"])

DROP_DETAIL_KEYS = {"error", "git_sha", "hostname", "invocation", "log"}


def previous_wall_times() -> dict[str, float]:
    if not ledger.exists():
        return {}
    text = ledger.read_text(encoding="utf-8", errors="replace")
    summaries = list(re.finditer(r"### Summary\n\n", text))
    if not summaries:
        return {}
    start = summaries[-1].end()
    end = text.find("\n### Details", start)
    if end == -1:
        end = len(text)
    lines = [line for line in text[start:end].splitlines() if line.startswith("|")]
    if len(lines) < 3:
        return {}
    header = [cell.strip() for cell in lines[0].strip("|").split("|")]
    try:
        scenario_idx = header.index("Scenario")
        wall_idx = header.index("Wall Time")
    except ValueError:
        return {}
    out = {}
    for line in lines[2:]:
        cells = [cell.strip().strip("`") for cell in line.strip("|").split("|")]
        if len(cells) <= max(scenario_idx, wall_idx):
            continue
        scenario = cells[scenario_idx].replace("\\|", "|")
        wall = cells[wall_idx]
        if wall.endswith("s"):
            wall = wall[:-1]
        try:
            out[scenario] = float(wall)
        except ValueError:
            pass
    return out


def read_jsonl(path: Path) -> list[dict]:
    rows = []
    with path.open("r", encoding="utf-8") as file:
        for line in file:
            line = line.strip()
            if not line:
                continue
            rows.append(json.loads(line))
    return rows


def fmt_seconds(value: float | None) -> str:
    if value is None:
        return "-"
    return f"{value:.3f}s"


def fmt_delta(current: float, previous: float | None) -> str:
    if previous is None:
        return "-"
    delta = current - previous
    sign = "+" if delta >= 0 else ""
    return f"{sign}{delta:.3f}s"


def table_escape(value: str) -> str:
    return value.replace("|", "\\|").replace("\n", " ")


def compact_row(row: dict) -> str:
    compact = {key: value for key, value in row.items() if key not in DROP_DETAIL_KEYS}
    return json.dumps(compact, sort_keys=True, separators=(",", ":"))


previous = previous_wall_times()
scenarios = []
with manifest.open("r", encoding="utf-8") as file:
    for line in file:
        name, invocation, status, elapsed, log, jsonl = line.rstrip("\n").split("\t")
        rows = read_jsonl(root / jsonl)
        harness = next(row for row in reversed(rows) if row.get("phase") == "harness")
        domain_rows = [row for row in rows if row.get("phase") != "harness"]
        scenarios.append(
            {
                "name": name,
                "invocation": invocation,
                "status": status,
                "wall": float(harness["wall_s"]),
                "previous": previous.get(name),
                "log": log,
                "jsonl": jsonl,
                "domain_rows": domain_rows,
                "harness": harness,
            }
        )

print()
print("---")
print()
print(f"## Run {os.environ['RUN_AT']} - {os.environ['RUN_LABEL']}")
print()
print(f"- result: `{os.environ['RUN_RESULT']}`")
print(f"- git: `{os.environ['RUN_SHA']}`")
print(f"- dirty: `{os.environ['RUN_DIRTY']}`")
print(f"- log_dir: `target/benchmark-smoke`")
print(f"- result_dir: `{result_dir.relative_to(root)}`")
print(f"- prebuild_s: `{float(os.environ['PREBUILD_S']):.3f}`")
print(f"- excerpt_lines: `{excerpt_lines}`")
if os.environ["RUN_NOTES"]:
    print(f"- notes: {os.environ['RUN_NOTES']}")

print()
print("### Summary")
print()
print("| Scenario | Status | Wall Time | Previous | Delta | JSONL Rows | Invocation |")
print("| --- | --- | ---: | ---: | ---: | ---: | --- |")
for scenario in scenarios:
    row_count = len(scenario["domain_rows"]) + 1
    print(
        "| "
        + " | ".join(
            [
                table_escape(scenario["name"]),
                f"`{scenario['status']}`",
                fmt_seconds(scenario["wall"]),
                fmt_seconds(scenario["previous"]),
                fmt_delta(scenario["wall"], scenario["previous"]),
                str(row_count),
                f"`{table_escape(scenario['invocation'])}`",
            ]
        )
        + " |"
    )

print()
print("### Details")
for scenario in scenarios:
    print()
    print(f"#### {scenario['name']}")
    print()
    print(f"- status: `{scenario['status']}`")
    print(f"- wall_time: `{fmt_seconds(scenario['wall'])}`")
    print(f"- previous_wall_time: `{fmt_seconds(scenario['previous'])}`")
    print(f"- delta: `{fmt_delta(scenario['wall'], scenario['previous'])}`")
    print(f"- log: `{scenario['log']}`")
    print(f"- jsonl: `{scenario['jsonl']}`")
    print("- invocation:")
    print()
    print("```sh")
    print(scenario["invocation"])
    print("```")
    print()
    print("- excerpt:")
    print()
    print("```jsonl")
    rows = scenario["domain_rows"][:excerpt_lines]
    if not rows:
        rows = [scenario["harness"]]
    for row in rows:
        line = compact_row(row)
        if len(line) > 1000:
            line = line[:1000] + "... [truncated; see jsonl]"
        print(line)
    print("```")
PY
}

