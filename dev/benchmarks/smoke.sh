  "jazz/sync" \
  "GROOVE_COMMITS=20 GROOVE_VIEW_EVERY=5 cargo bench -p jazz --bench sync" \
  env GROOVE_COMMITS=20 GROOVE_VIEW_EVERY=5 cargo bench -p jazz --bench sync

run_scenario \
  "jazz/validation" \
  "GROOVE_CLIENTS=5 GROOVE_ROWS=20 GROOVE_COMMITS=25 cargo bench -p jazz --bench validation" \
  env GROOVE_CLIENTS=5 GROOVE_ROWS=20 GROOVE_COMMITS=25 cargo bench -p jazz --bench validation

run_scenario \
  "jazz/merge_back_cost" \
  "JAZZ_MERGE_BACK_WRITES=50 cargo bench -p jazz --bench merge_back_cost" \
  env JAZZ_MERGE_BACK_WRITES=50 cargo bench -p jazz --bench merge_back_cost

run_scenario \
  "jazz/large_value_checkpointing" \
  "cargo bench -p jazz --bench large_value_checkpointing" \
  cargo bench -p jazz --bench large_value_checkpointing

run_scenario \
