#!/usr/bin/env bash
set -u -o pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
LEDGER="${SMOKE_LEDGER:-$ROOT/dev/benchmarks/SMOKE_LEDGER.md}"
LOG_DIR="${SMOKE_LOG_DIR:-$ROOT/target/benchmark-smoke}"
RUN_LABEL="${SMOKE_LABEL:-smoke}"
RUN_NOTES="${SMOKE_NOTES:-}"
EXCERPT_LINES="${SMOKE_LEDGER_EXCERPT_LINES:-18}"

mkdir -p "$LOG_DIR"

failures=()
scenario_names=()
scenario_invocations=()
scenario_statuses=()
scenario_elapsed=()
scenario_logs=()

escape_table_cell() {
  local value="$1"
  value="${value//$'\n'/ }"
  value="${value//|/\\|}"
  printf '%s' "$value"
}

git_dirty() {
  if git -C "$ROOT" diff --quiet && git -C "$ROOT" diff --cached --quiet; then
    printf 'false'
  else
    printf 'true'
  fi
}

metrics_excerpt() {
  local log="$1"
  local filtered

  if [[ ! -s "$log" ]]; then
    printf '(log is empty)\n'
    return
  fi

  if grep -q "panicked at\\|thread '.*' panicked" "$log"; then
    grep -A6 -B2 "panicked at\\|thread '.*' panicked" "$log" | tail -n "$EXCERPT_LINES"
    return
  fi

  if grep -q "assertion .*failed\\|assertion failed\\|error: bench failed" "$log"; then
    grep -E "assertion .*failed|assertion failed|error: bench failed" "$log" | tail -n "$EXCERPT_LINES"
    return
  fi

  filtered="$(
    grep -E '(^\{|^depth |^large_value_checkpointing|^[[:alnum:]_./ -]+[=:]|_us|_ms|_ns|bytes|rows|commits|throughput|scenario|phase|status|needs)' "$log" \
      | grep -Ev '^(warning:|note:|[[:space:]]*= note:|Compiling |Finished |Running |Benchmarking |test )' \
      | tail -n "$EXCERPT_LINES" \
      | python3 -c 'import json, sys
drop = {"error", "git_sha", "hostname"}
for line in sys.stdin:
    text = line.rstrip("\n")
    try:
        obj = json.loads(text)
    except json.JSONDecodeError:
        print(text)
        continue
    if isinstance(obj, dict):
        obj = {k: v for k, v in obj.items() if k not in drop}
        print(json.dumps(obj, sort_keys=True, separators=(",", ":")))
    else:
        print(text)
' \
      | awk '{ if (length($0) > 1000) print substr($0, 1, 1000) "... [truncated; see log]"; else print }'
  )"

  if [[ -n "$filtered" ]]; then
    printf '%s\n' "$filtered"
  else
    printf '(no concise metrics emitted; see log)\n'
  fi
}

append_ledger_header_if_needed() {
  if [[ -s "$LEDGER" ]]; then
    return
  fi

  cat >"$LEDGER" <<'LEDGER'
# Benchmark Smoke Ledger

Append-only smoke benchmark runs generated by `dev/benchmarks/smoke.sh`.

Each run keeps a compact status summary plus per-scenario excerpts. Full raw
stdout/stderr logs live under `target/benchmark-smoke/` and are intentionally
kept out of this file so the ledger stays readable in code review and by LLMs.
LEDGER
}

record_scenario() {
  local name="$1"
  local invocation="$2"
  local status="$3"
  local elapsed="$4"
  local log="$5"

  scenario_names+=("$name")
  scenario_invocations+=("$invocation")
  scenario_statuses+=("$status")
  scenario_elapsed+=("$elapsed")
  scenario_logs+=("$log")
}

run_scenario() {
  local name="$1"
  local invocation="$2"
  shift 2
  local log="$LOG_DIR/${name//[^A-Za-z0-9_.-]/_}.log"
  local start end elapsed status status_text

  printf '==> %s\n' "$name"
  start="$(perl -MTime::HiRes=time -e 'print time')"
  (
    cd "$ROOT"
    "$@"
  ) >"$log" 2>&1
  status=$?
  end="$(perl -MTime::HiRes=time -e 'print time')"
  elapsed="$(awk -v start="$start" -v end="$end" 'BEGIN { printf "%.3fs", end - start }')"

  if [[ $status -eq 0 ]]; then
    status_text="pass"
  elif grep -q "panicked at\\|thread '.*' panicked" "$log"; then
    status_text="panic"
  else
    status_text="fail"
  fi

  record_scenario "$name" "$invocation" "$status_text" "$elapsed" "$log"

  if [[ $status -ne 0 ]]; then
    failures+=("$name ($status_text)")
  fi
}

note_scenario() {
  local name="$1"
  local invocation="$2"
  local note="$3"
  local log="$LOG_DIR/${name//[^A-Za-z0-9_.-]/_}.log"
  printf '%s\n' "$note" >"$log"
  record_scenario "$name" "$invocation" "not-run" "-" "$log"
}

append_ledger_run() {
  local run_at sha dirty result rel_log idx
  run_at="$(date -u +%Y-%m-%dT%H:%M:%SZ)"
  sha="$(git -C "$ROOT" rev-parse --short HEAD)"
  dirty="$(git_dirty)"
  if ((${#failures[@]})); then
    result="fail"
  else
    result="pass"
  fi

  append_ledger_header_if_needed

  {
    printf '\n---\n\n'
    printf '## Run %s - %s\n\n' "$run_at" "$RUN_LABEL"
    printf '%s\n' "- result: \`$result\`"
    printf '%s\n' "- git: \`$sha\`"
    printf '%s\n' "- dirty: \`$dirty\`"
    printf '%s\n' "- log_dir: \`target/benchmark-smoke\`"
    printf '%s\n' "- excerpt_lines: \`$EXCERPT_LINES\`"
    if [[ -n "$RUN_NOTES" ]]; then
      printf '%s\n' "- notes: $RUN_NOTES"
    fi

    printf '\n### Summary\n\n'
    printf '| Scenario | Status | Wall Time | Invocation |\n'
    printf '| --- | --- | ---: | --- |\n'
    for idx in "${!scenario_names[@]}"; do
      printf '| %s | `%s` | %s | `%s` |\n' \
        "$(escape_table_cell "${scenario_names[$idx]}")" \
        "${scenario_statuses[$idx]}" \
        "${scenario_elapsed[$idx]}" \
        "$(escape_table_cell "${scenario_invocations[$idx]}")"
    done

    printf '\n### Details\n'
    for idx in "${!scenario_names[@]}"; do
      rel_log="${scenario_logs[$idx]#$ROOT/}"
      printf '\n#### %s\n\n' "${scenario_names[$idx]}"
      printf '%s\n' "- status: \`${scenario_statuses[$idx]}\`"
      printf '%s\n' "- wall_time: \`${scenario_elapsed[$idx]}\`"
      printf '%s\n' "- log: \`$rel_log\`"
      printf '%s\n\n' "- invocation:"
      printf '```sh\n%s\n```\n\n' "${scenario_invocations[$idx]}"
      printf '%s\n\n' "- excerpt:"
      printf '```text\n'
      metrics_excerpt "${scenario_logs[$idx]}"
      printf '```\n'
    done
  } >>"$LEDGER"
}

